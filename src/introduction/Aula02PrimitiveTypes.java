package introduction;

public class Aula02PrimitiveTypes {
    public static void main(String[] args) {
        // int, double, float, char, byte, short, long, boolean

        int age = 23;
        long bigNumber = 100000;
        double salaryDouble = 2000.5;
        float salaryFloat = 2500.5f;
        byte ageByte = 23;
        short ageShort = 23;
        boolean haveSalary = true;
        char character = 'c';

        System.out.println(age);
        System.out.println(bigNumber);
        System.out.println(salaryDouble);
        System.out.println(salaryFloat);
        System.out.println(ageByte);
        System.out.println(ageShort);
        System.out.println(haveSalary);
        System.out.println(character);

    }
}