package introduction;

public class Aula06Switch {

    public static void main(String[] args) {

        int age = 11;

        switch (age){

            case 1:
                System.out.println("None");
                break;
            case 2:
                System.out.println("None");
                break;
            case 3:
                System.out.println("None");
                break;
            case 4:
                System.out.println("None");
                break;
            case 7:
                System.out.println("None");
                break;
            case 9:
                System.out.println("None");
                break;
            case 10:
                System.out.println("Match!");
                break;
            default:
                System.out.println("Not match... :(");
        }
    }
}
